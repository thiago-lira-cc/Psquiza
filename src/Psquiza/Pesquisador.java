package Psquiza;

public class Pesquisador {
	private String nome;
	private String funcao;
	private String biografia;
	private String email;
	private String foto;
	
	public Pesquisador(String nome, String funcao, String biografia, String email, String foto) {
		this.nome = nome;
		this.funcao = funcao;
		this.biografia = biografia;
		this.email = email;
		this.foto = foto;
	}
}
